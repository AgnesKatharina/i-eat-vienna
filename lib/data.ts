// Function to parse weight
export function parseWeight(value: string): number {
  value = value.trim().toLowerCase()

  if (value.includes("kg")) {
    const kgValue = Number.parseFloat(value.replace("kg", "").trim())
    return Math.round(kgValue * 1000)
  } else if (value.includes("g")) {
    const gValue = Number.parseFloat(value.replace("g", "").trim())
    return Math.round(gValue)
  } else if (value.includes("l")) {
    const lValue = Number.parseFloat(value.replace("l", "").trim())
    return Math.round(lValue * 1000)
  } else if (value.includes("ml")) {
    const mlValue = Number.parseFloat(value.replace("ml", "").trim())
    return Math.round(mlValue)
  } else {
    throw new Error(`Invalid input: ${value}. Please use 'g', 'kg', 'ml', or 'l'.`)
  }
}

